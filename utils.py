"""
Utility functions for mathematical operations.
This module provides helper functions used in unit testing.
"""


def add_numbers(first_value, second_value):
    """
    Performs addition of two numeric values.
    
    Args:
        first_value (int/float): First number to add
        second_value (int/float): Second number to add
    
    Returns:
        int/float: Sum of the two input values
    """
    return first_value + second_value


def multiply_numbers(first_value, second_value):
    """
    Performs multiplication of two numeric values.
    
    Args:
        first_value (int/float): First number to multiply
        second_value (int/float): Second number to multiply
    
    Returns:
        int/float: Product of the two input values
    """
    return first_value * second_value
